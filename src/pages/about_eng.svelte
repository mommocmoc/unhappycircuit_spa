            <p>
                ＜A Interstellar Message Written in Hangeul＞ is a new interstellar message designed to communicate
                with extraterrestrial civilizations that utilize radio communication. In particular, this message is
                the first Interstellar message to apply Korean characters 'Hangeul' and grammar as an attempt to
                deliver the Earth's natural language to extraterrestrial intelligence. This is an effort to more
                systematically convey information about the language of the Earth towards the unknown, and at the
                same time emphasize the importance of reflecting the diversity of languages and characters on the
                planet, including Hangeul and Korean, in designing interstellar messages.
            </p>
            <p>
                The overall structure of this message is based on ＜Cosmic Call＞ designed in 1999 and 2003, but the
                overall contents have been revised and new contents have been added. Above all, this message is
                significant as the first attempt to combine the prior interstellar message, which was studied mainly
                on the expression of the Latin alphabet and mathematical symbols, with the Earth's natural language.
            </p>
            <p>
                In particular, this message excludes the use of Chinese character 'Hanja' expressions, which occupy
                a large portion of the modern Korean, and applies newly designed pure Korean expressions. Most of
                the mathematics and scientific terms used in Korea use Hanja expressions, but the pure Korean
                expressions used in this message conveys the meaning of the term more easily and clearly than Hanja
                expressions. As a result, it effectively reflects the advantages of Hangul characters.
                Some proper nouns and units should be written based on the modern Korean notation and the
                International System of Units.
            </p>
            <p>
                This message was designed in a way to define the concept step by step. It is composed of a structure
                that allows you to understand the next content if you understand the previous content. And because
                it is written based on basic math and scientific knowledge, by following the messages step by step
                in order, extraterrestrial astronomers and linguists will be able to understand the message.
            </p>
            <p>
                As expressed by Dr. Yvan Dutil, who designed ＜Cosmic Call＞, this message is “Interstellar Rosetta
                Stone” written in Korean that contains brief and essential information about the knowledge and
                language of the Earth. Above all, in the last paragraph of this message, there is a one sentence
                that reflects our human beings. This last message is humanity's message to outer space, and at the
                same time, it is a message directed to ourselves.
            </p>
            <p>
                The website of ＜A Interstellar Message Written in Hangeul> was first released on Hangeul Day on
                October 9, 2020. In addition, the ＜A Interstellar Message Written in Hangeul＞ was simultaneously
                produced as an audio visual installation and exhibited at the National Library of Korea and Hyundai
                Motor Group’s ZER01NE Studio in 2020.
