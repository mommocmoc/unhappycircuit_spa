                ＜한글로 쓰인 별 사이 쪽글＞의 웹사이트는 2020년 10월 9일 한글날에 처음 공개되었다. 또한 오디오 비주얼 인스톨레이션 형태의 작품으로도 동시에 제작되어
                국립중앙도서관과 제로원 오픈스튜디오에서 2020년에 전시되었다.
