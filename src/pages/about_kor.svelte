            <p>
                ＜한글로 쓰인 별 사이 쪽글＞은 전파통신을 활용하는 외계문명의 존재를 가정하고 그들과의 소통을 위해 디자인된 새로운 '인터스텔라 메시지(Interstellar
                message)'이다. 특히 본 메시지는 지구의 자연어를 외계지적생명체에게 전달하고자 하는 시도로써 한글 문자와 한국어 문법을 적용한 첫 번째 인터스텔라 메시지이다.
                이는 미지의 존재를 향해 지구의 언어에 대한 정보를 보다 체계적으로 전달하려는 노력인 동시에 인터스텔라 메시지 디자인에 한글과 한국어를 포함한 지구상의 여러 언어와
                그 문자들의 다양성을 반영하는 것이 중요함을 강조하기 위함이다.
            </p>
            <p>
                본 메시지의 전체적인 구조는 1999년과 2003년에 디자인된 ＜코스믹 콜(Cosmic Call)＞을 기반으로 하고 있으나, 전반적인 내용이 수정되었으며 새로운
                내용이 추가되었다. 무엇보다 본 메시지는 로마 알파벳과 수학적 기호 표현을 위주로 연구된 기존의 인터스텔라 메시지를 확장하여 지구의 자연어 및 문자 체계와 결합하려는
                첫 시도로써 그 의의가 있다.
            </p>
            <p>
                특히 본 메시지는 현대 한국어의 많은 비중을 차지하고 있는 한자식 표현의 사용을 배제하고 새롭게 고안한 순우리말 표현을 적용하였다. 한국에서 쓰이는 수학 및 과학
                용어들은 대부분 한자식 표현을 따르고 있는데, 본 메시지에서 쓰인 순우리말 표현의 경우 한자식 표현보다 그 용어가 가지는 의미를 더 쉽고 분명하게 전달함으로써, 한글
                문자의 고유한 특성을 효과적으로 드러낼 수 있다. 그 외 몇몇 고유명사 및 단위들은 현대 한국어 표기법 및 국제단위계에 근거하여 작성하는 것을 원칙으로 하였다.
            </p>
            <p>
                본 메시지는 단계적으로 개념을 정의해나가는 방식으로 구성되어 있다. 앞선 내용을 이해하면 그다음 내용을 이해할 수 있는 구조로 이루어져 있으며, 기본적인 수학 및
                과학 지식에 대한 내용을 중심으로 작성되어 있다. 메시지를 순서대로 차근차근 따라가다 보면, 외계의 천문학자와 언어학자들은 이 메시지를 충분히 이해할 수 있을
                것이다.
            </p>
            <p>
                ＜코스믹 콜＞을 디자인한 얀 듀틸(Yvan Dutil) 박사의 표현처럼, 본 메시지는 지구의 지식과 언어에 대한 간략하면서도 핵심적인 정보를 담고 있는 한글로 작성된
                '인터스텔라 로제타 스톤(Rosetta Stone)’이다. 무엇보다 본 메시지의 맨 마지막 단락에는 우리 인간의 현재 모습이 반영된 한 문장의 메시지가 작성되어있다.
                이 마지막 메시지는 먼 우주를 향하는 인류의 메시지이자, 동시에 바로 우리 자신을 향하고 있는 메시지이다.
            </p>
