        <div class="main">
            <p>
                언해피서킷(Unhappy Circuit)은 과학과 예술의 융합을 중심으로 한 작품 활동을 이어오고 있는 다학제 및 뉴미디어 예술가이다. 컴퓨터의
                언어에서부터
                인공지능과 고래 그리고 외계지적생명체의 언어까지 그의 작업은 ‘언어’를 통해 미지의 존재를 탐구하는 과정이다. 인간이 아닌 존재를 탐구함으로써 그는
                우리 인간의
                진실한 모습을 발견하고자 하며, 이를 통해 인류가 나아갈 다음 방향을 함께 모색하고자 하는 것이다.
            </p>
            <p>
                인간의 언어와 컴퓨터의 언어를 매개하는 오디오 비주얼 작품(Music of Memories, 국립아시아문화전당, 2017)에서부터 인간의 기억을 학습한
                인공지능의
                정체성은 과연 무엇인가를 묻는 오디오 비주얼 작품(i Remember, 한국문화예술위원회, 2018), 인공지능이 생성한 새로운 음식 레시피를 요리하는
                퍼포먼스
                작품(Learning About Humanity, 국립아시아문화전당, 2019), 고래의 소리와 인간의 음악을 합성한 인공지능 음악 작품(A
                Synthetic
                Song Beyond the Sea, 아트센터나비, 2019), 외계지적생명체 탐사를 위한 전파 신호 관측 데이터 오디오 비주얼라이제이션 작품(A
                Letter
                Across the Stars, 국립아시아문화전당, 2019, SIGGRAPH Asia, Fact Magazine, 2020) 그리고 ＜한글로 쓰인 별
                사이
                쪽글＞까지, 그의 작업은 언어와 음악, 기억과 데이터 그리고 인간과 우주를 연결한다.
            </p>
